CREATE TABLE OAUTH2_REGISTERED_CLIENT (
    ID                            VARCHAR(100)  NOT NULL,
    CLIENT_ID                     VARCHAR(100)  NOT NULL,
    CLIENT_ID_ISSUED_AT           TIMESTAMP     DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CLIENT_SECRET                 VARCHAR(200),
    CLIENT_SECRET_EXPIRES_AT      TIMESTAMP,
    CLIENT_NAME                   VARCHAR(200)  NOT NULL,
    CLIENT_AUTHENTICATION_METHODS VARCHAR(1000) NOT NULL,
    AUTHORIZATION_GRANT_TYPES     VARCHAR(1000) NOT NULL,
    REDIRECT_URIS                 VARCHAR(1000),
    POST_LOGOUT_REDIRECT_URIS     VARCHAR(1000),
    SCOPES                        VARCHAR(1000) NOT NULL,
    CLIENT_SETTINGS               VARCHAR(2000) NOT NULL,
    TOKEN_SETTINGS                VARCHAR(2000) NOT NULL,
    CONSTRAINT PK_OAUTH2_RC PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX UQ_OAUTH2_RC_CLIENT_ID ON OAUTH2_REGISTERED_CLIENT (CLIENT_ID);

CREATE TABLE OAUTH2_AUTHORIZATION_CONSENT (
    REGISTERED_CLIENT_ID VARCHAR(100)  NOT NULL,
    PRINCIPAL_NAME       VARCHAR(200)  NOT NULL,
    AUTHORITIES          VARCHAR(1000) NOT NULL,
    CONSTRAINT PK_OAUTH2_AC PRIMARY KEY (REGISTERED_CLIENT_ID, PRINCIPAL_NAME)
);

-- BLOB columns store serialized token data (base64 encoded in practice)
-- 65536 = 64KB per field which covers most JWT token use cases
CREATE TABLE OAUTH2_AUTHORIZATION (
    ID                            VARCHAR(100)  NOT NULL,
    REGISTERED_CLIENT_ID          VARCHAR(100)  NOT NULL,
    PRINCIPAL_NAME                VARCHAR(200)  NOT NULL,
    AUTHORIZATION_GRANT_TYPE      VARCHAR(100)  NOT NULL,
    AUTHORIZED_SCOPES             VARCHAR(1000),
    ATTRIBUTES                    BLOB(65536),
    STATE                         VARCHAR(500),
    AUTHORIZATION_CODE_VALUE      BLOB(65536),
    AUTHORIZATION_CODE_ISSUED_AT  TIMESTAMP,
    AUTHORIZATION_CODE_EXPIRES_AT TIMESTAMP,
    AUTHORIZATION_CODE_METADATA   BLOB(65536),
    ACCESS_TOKEN_VALUE            BLOB(65536),
    ACCESS_TOKEN_ISSUED_AT        TIMESTAMP,
    ACCESS_TOKEN_EXPIRES_AT       TIMESTAMP,
    ACCESS_TOKEN_METADATA         BLOB(65536),
    ACCESS_TOKEN_TYPE             VARCHAR(100),
    ACCESS_TOKEN_SCOPES           VARCHAR(1000),
    OIDC_ID_TOKEN_VALUE           BLOB(65536),
    OIDC_ID_TOKEN_ISSUED_AT       TIMESTAMP,
    OIDC_ID_TOKEN_EXPIRES_AT      TIMESTAMP,
    OIDC_ID_TOKEN_METADATA        BLOB(65536),
    REFRESH_TOKEN_VALUE           BLOB(65536),
    REFRESH_TOKEN_ISSUED_AT       TIMESTAMP,
    REFRESH_TOKEN_EXPIRES_AT      TIMESTAMP,
    REFRESH_TOKEN_METADATA        BLOB(65536),
    USER_CODE_VALUE               BLOB(65536),
    USER_CODE_ISSUED_AT           TIMESTAMP,
    USER_CODE_EXPIRES_AT          TIMESTAMP,
    USER_CODE_METADATA            BLOB(65536),
    DEVICE_CODE_VALUE             BLOB(65536),
    DEVICE_CODE_ISSUED_AT         TIMESTAMP,
    DEVICE_CODE_EXPIRES_AT        TIMESTAMP,
    DEVICE_CODE_METADATA          BLOB(65536),
    CONSTRAINT PK_OAUTH2_AUTH PRIMARY KEY (ID)
);
